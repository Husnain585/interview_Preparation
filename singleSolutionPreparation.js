// Linked LIst => A collection of Nodes, where each node consist of two parts, one part has the data and second part
// has the address which refer to next node in the linkedlist 

// Stack => A stack is a linear data structure that follows the Last In, First Out (LIFO) principle, 
// where elements are added and removed from the same end called the top. (push, pop, peek)

// Queue => A Queue is a linear data structure that follows the First In, First out (FIFO) principle,
// where elements are added and removed from the same end called the top. (enqueue, dequeue, peek)

// Hash Table => A Hash Table is a data structure designed to be fast to work.
// ${`The reason Hash Tables are sometimes preferred instead of arrays or linked lists is because searching for, adding, and deleting data can be done really quickly, even for large amounts of data.`}

// Tree => A Non-linear data sturcture where data is representing in the form of Nodes and attach with edges,
//             Mainly Root Node represent parent and leave node represent to child which create a hierarchical.

// Graph  => A Non-Linear data structure which consist on set of node connected with the help of edges which can directed or undirected.

